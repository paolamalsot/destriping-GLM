def get_index_gt_destriped(dataset_path, df, gt_destriping_method = "qm"):
    name_dist, _ = convert_gen_params_to_qm_params(
        *generating_data_distribution(dataset_path))
    model_name = f"GT_{name_dist}_sol"
    destriping_method_full_name = (
        f"destripe_dividing_factors_{gt_destriping_method}_tot_counts"
    )
    index_gt = df.query(
        "(model_name == @model_name) and (destriping_method == @destriping_method_full_name)"
    ).index[0]
    return index_gt

def build_df_from_sdata(dataset_path: str, cell_id_label: str) -> pd.DataFrame:
    """
    Rebuild the GLUM design DataFrame from a spatialAdata object.

    Mirrors the logic in ``GlumWrapper.df_from_sdata``:
    - adds array coordinates,
    - drops rows with missing cell ids,
    - prefixes cell ids with ``\"id_\"``,
    - calls ``format_obs_to_df``.
    """
    sdata = load_spatialAdata(dataset_path)
    sdata.add_array_coords_to_obs()
    obs = sdata.obs
    obs = obs.query(f"~{cell_id_label}.isna()").copy()
    obs[cell_id_label] = obs[cell_id_label].astype(str)
    obs[cell_id_label] = "id_" + obs[cell_id_label]
    df = format_obs_to_df(obs, cell_id_label)
    df.reset_index(drop=True, inplace=True)
    return df


def load_gt_sol(dataset_root: Path) -> Sol | None:
    """
    Load the ground-truth PoissonSol for a simulated dataset, if available.

    For simulation datasets generated by
    ``experiments/src/custom_destriping_benchmark/simulation_generate_data.py``,
    the solution is stored in ``<dataset_root>/gt_sol``.
    """
    gt_dir = P(dataset_root) / "gt_sol"
    if not gt_dir.exists():
        return None
    return Sol.load(str(gt_dir))


def get_dataset_path(df):
    dataset_paths = pd.unique(df["dataset_path"])
    assert len(dataset_paths) == 1
    return dataset_paths[0]


def get_path_dataset(row):
    if not (pd.isna(row.config)):
        if type(row.config) == str:
            config = ast.literal_eval(row.config)
        else:
            config = row.config
        return config["dataset"]["path_data"]  # this is the path to the dataset
    else:
        return pd.NA


def get_cell_id_label(df):
    cell_id_labels = pd.unique(df["cell_id_label"])
    assert len(cell_id_labels) == 1
    return cell_id_labels[0]


def load_poisson_sol(row):
    poisson_sol_path = row["poisson_sol_path"]
    if not(pd.isna(poisson_sol_path)):
        sol = Sol.load(poisson_sol_path)
    else:
        sol = None
    return sol


def get_destriped_tot_counts_from_df(df, take_n_counts_adjusted = False):
    if take_n_counts_adjusted and "n_counts_adjusted" in df.columns:
        return df["n_counts_adjusted"]
    return df["n_counts"]


def get_df_destriped_data_from_row(row):
    path = row["destriped_data_path"]
    df_path = Path(path)
    df_destriped_data = pd.read_parquet(df_path)
    return df_destriped_data


def get_destriped_tot_counts(row, take_n_counts_adjusted = False):
    df = get_df_destriped_data_from_row(row)
    return get_destriped_tot_counts_from_df(df, take_n_counts_adjusted = take_n_counts_adjusted)
